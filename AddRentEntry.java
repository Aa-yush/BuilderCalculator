    private void setCustomer(){
    try{
           Class.forName("com.mysql.jdbc.Driver");
           Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/pawandb","root","");
           PreparedStatement ps=con.prepareStatement("select CustName from RentalCustomerDetails");
           ResultSet rs=ps.executeQuery();
           while(rs.next()){
          selectcustomer.addItem(rs.getString("CustName"));
           }
       }catch(Exception e){
           JOptionPane.showMessageDialog(null, "Problem in to assign values in ComboBox");
       }
    }
