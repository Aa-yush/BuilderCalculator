    private void addentrybuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addentrybuttonActionPerformed
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/pawandb","root","");
            PreparedStatement ps=con.prepareStatement("insert into RentCustEntry values(?,?,?,?,?,?,?,?)");
            DateFormat df=new SimpleDateFormat("yyyy-MM-dd");
            ps.setString(1, df.format(selectdate.getDate()));
            ps.setInt(2, Integer.parseInt(selecteno.getText()));
            ps.setInt(3,Integer.parseInt(selectcquantity.getText()));
            String cust=selectcustomer.getSelectedItem().toString();
            ps.setString(4, cust);
            String prod=selectproduct.getSelectedItem().toString();
           ps.setString(5, prod);
           ps.setString(6,selectplotnumber.getText());
           ps.setString(7,selectstockname.getText());
           ps.setInt(8, Integer.parseInt(selectrent.getText()));
           ps.execute();
            JOptionPane.showMessageDialog(null, "Your entry added Successfully");
          }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Please !! Fill Correct Entries");
        }
    }//GEN-LAST:event_addentrybuttonActionPerformed

